    public void setFormData(String id, String email, String password, String nama, String telpon, String role, String status) {
        txtEmail.setText(email);
        txtPassword.setText(password);
        txtNama.setText(nama);
        txtTelepon.setText(telpon);
        dropRole.setSelectedItem(role);
        dropStatus.setSelectedItem(status);

        btnSimpan.setText("Update");
        btnSimpan.removeActionListener(btnSimpan.getActionListeners()[0]);
        btnSimpan.addActionListener(evt -> {
            String sql = "UPDATE karyawan SET email=?, password=?, nama_karyawan=?, no_tlp=?, role=?, status=? WHERE id_karyawan=?";
            try {
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setString(1, txtEmail.getText());
                stmt.setString(2, md5.md5(txtPassword.getText()));
                stmt.setString(3, txtNama.getText());
                stmt.setString(4, txtTelepon.getText());
                stmt.setString(5, dropRole.getSelectedItem().toString());
                stmt.setString(6, dropStatus.getSelectedItem().toString());
                stmt.setString(7, id);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data diupdate!");
                if (onDataSaved != null) {
                    onDataSaved.run();
                }
                dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Update gagal: " + ex.getMessage());
            }
        });
    }

