    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String email = txtEmail.getText();
        String password = md5.md5(txtPassword.getText());
        String nama = txtNama.getText();
        String telpon = txtTelepon.getText();
        String role = dropRole.getSelectedItem().toString();
        String status = dropStatus.getSelectedItem().toString();
        String idBaru = IDGenerator.generateAutoID(conn, "karyawan", "id_karyawan", "KRY", 3);   
        String sql = "INSERT INTO karyawan (email, password, nama_karyawan, no_tlp, role, status, id_karyawan) VALUES (?, ?, ?, ?, ?, ?, ?)";

        try {
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, email);
            stmt.setString(2, password);
            stmt.setString(3, nama);
            stmt.setString(4, telpon);
            stmt.setString(5, role);
            stmt.setString(6, status);
            stmt.setString(7, idBaru);

            int rowsInserted = stmt.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
                txtEmail.setText("");
                txtPassword.setText("");
                txtNama.setText("");
                txtTelepon.setText("");
                dropRole.setSelectedIndex(0);
                dropStatus.setSelectedIndex(0);

                if (onDataSaved != null) {
                    onDataSaved.run();
                }
                dispose(); 
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan data: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

